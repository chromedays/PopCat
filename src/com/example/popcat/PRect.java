package com.example.popcat;

public class PRect {
	public float left;
	public float top;
	public float right;
	public float bottom;
	public PRect()
	{
		
	}
	public PRect(float left,float top,float right,float bottom)
	{
		this.left = left;
		this.top = top;
		this.right = right;
		this.bottom = bottom;
	}
}
